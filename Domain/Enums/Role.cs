namespace Domain.Enums
{
    /// <summary>
    /// Represents an enumeration of user roles.
    /// Представляет перечисление ролей пользователей.
    /// </summary>
    public enum Role
    {
        /// <summary>
        /// An enumeration element that represents the user's role.
        /// Элемент перечисления, представляющий роль пользователя.
        /// </summary>
        User = 1,
        /// <summary>
        /// An enumeration element that represents the administrator role.
        /// Элемент перечисления, представляющий роль администратора.
        /// </summary>
        Admin = 2,
    }
}
